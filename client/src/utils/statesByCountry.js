/**
 * Get states/provinces for a specific country
 * @param {string} country - Country name
 * @returns {Array} Array of state objects { code, name } or empty array if country not found
 */
export const getStatesForCountry = (country) => {
  if (!country) return [];
  return statesByCountry[country] || [];
};

/**
 * Get all countries that have states defined
 * @returns {Array} Array of country names
 */
export const getCountriesWithStates = () => {
  return Object.keys(statesByCountry);
};

